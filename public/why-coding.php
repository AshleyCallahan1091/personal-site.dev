						<p class="blog-left"><span class="blog">Let&#39;s start by painting a picture.</span> Nine months ago, my husband went off to training for an entire month. I asked him if there was anything he needed me to do while he was gone and he said &quot;Get a job.&quot; He was only joking, of course. My husband is not a third derivative &#40;weak calculus joke&#41; and I knew he was only joking. That month of training did not allow for overtime so money would be a little tight but it wasn&#39;t a necessity for me to start working again. Even still, I went out and got a job that day.</p><br>

						<p class="blog-left"> <span class="blog">It wasn&#39;t a great job;</span> I handed out samples of frozen yogurt for 3-7 hours a day and fielded questions like &quot;I&#39;m on a diet and need something sugar-free and fat-free with the smallest amount of calories so what are my options?&quot; I stayed there for six months, making just above minimum wage and ended up working a couple different side jobs to bring in more income when my husband and I moved into our own apartment.</p><br>

						<p class="blog-left"><span class="blog">After a particularly frustrating day </span>between cleaning houses and being on my feet all day, I passed the &quot;Learn to Code - Codeup&quot; billboard for the thirtieth time and thought to myself, &quot;I can see myself learning to code. What else have I got to lose?&quot;</p><br>

						<p class="blog-left"><span class="blog">I took a web development class in high school </span>and tinkered with HTML, Photoshop and Dreamweaver and it was pretty fun. I didn&#39;t even think to pursue it in college because it was almost too much fun and high school teaches you pretty early on that your career isn&#39;t supposed to be fun if you want to actually earn money. Of course, this is coming from someone who spent two years as a Liberal Arts major in college and look how well that worked out for me - serving FroYo samples for $7.75/hr, surrounded by high school kids.</p><br>

						<p class="blog">So, in all honesty, I really had nothing left to lose.</p><br>

						<p class="blog-left"><span class="blog">I sent in a request for more information </span>and within the next couple days, I was on the phone with Dylan, asking any questions I could think of while I was on my way to work. I submitted my application that week and less than a month later, I found myself in the Codeup classroom learning a new set of skills.</p><br>

						<p class="blog">But why Codeup? Why not a computer science degree?</p><br>

						<p class="blog-left"><span class="blog">Well, why would I spend four years learning a set of skills I can knock out in four months </span>for a fraction of the cost without the hassle of schedules and classes I don&#39;t even need specifically for coding just to maintain full-time student status? I have nothing against college. I have some awesome memories from Northeast Lakeview. I learned some very interesting skills but the only real thing I have to show for it is my charcoal self-portrait on my homepage. What Codeup offers is the potential for a full-time career you&#39;ll love at the end of the program, not the necessity for a full-time job in any field just to pay off an enormous amount of student loan debt. Four months is a perfect amount of time because there isn&#39;t time to get burned out. You don&#39;t have to drag yourself out of bed for four years, wondering if you even want to stick with your degree.</p><br>

						<p class="blog-left"><span class="blog">Isn&#39;t four months an insane amount of time to learn such a large quantity of material and topics? </span>Absolutely. Is it doable? You bet. Will it be hard? Yes. Especially so if you have a family. There will be missed family dinners, less playtime, less dates and more late nights than not. But it&#39;s only four months. If you can sacrifice sixteen weeks, you&#39;ll get back a lifetime of opportunities to support your family while doing something you&#39;re passionate about and live the life you want because you had the guts to tackle learning a skill set in a bootcamp instead of the path of traditional education.</p><br>

